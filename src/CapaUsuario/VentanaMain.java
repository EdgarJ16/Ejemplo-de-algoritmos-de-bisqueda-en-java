    private void botonShellActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonShellActionPerformed
        try {
            //SE UTILIZA EL METODO ARRAYS COPY OF SOLO PARA GUARDAR LOS VALORES DEL ARREGLO ORIGINAL NO PARA ORDENARLO
            copiaVector = Arrays.copyOf(generaArreglo.getArreglo(), 1000);

            //UNA VEZ ALMACENADO SE PROCEDE A ORDENARLO
            long inicio = System.nanoTime();
            generaArreglo.shellSort(copiaVector);
            long fin = System.nanoTime();

            tiempoTotal = (fin - inicio);

        } catch (Exception e) {

        }
        //tiempoTotal = tiempoTotal/100; //TIEMPO PROMEDIADO EN MILLISEGUNDOS
        botonConfirmar.setEnabled(true);
        opArreglo = 2;// TODO add your handling code here:
    }//GEN-LAST:event_botonShellActionPerformed

    private void botonConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonConfirmarActionPerformed
        txtOrdenado.setText(imprimeOrdenado(copiaVector));
        labelnano.setVisible(true);
        switch (opArreglo) {
            case 1:
                nombreAlgoritmo.setText("Burbuja");
                tiempo.setText(String.valueOf(tiempoTotal));
                break;
            case 2:
                nombreAlgoritmo.setText("ShellSort");
                tiempo.setText(String.valueOf(tiempoTotal));
                break;
            case 3:
                nombreAlgoritmo.setText("QuickSort");
                tiempo.setText(String.valueOf(tiempoTotal));
                break;

        }
        botonConfirmar.setEnabled(false);


    }//GEN-LAST:event_botonConfirmarActionPerformed

    private void botonQuickActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonQuickActionPerformed
        try {
            copiaVector = Arrays.copyOf(generaArreglo.getArreglo(), 1000);
            
            //UNA VEZ ALMACENADO SE PROCEDE A ORDENARLO
            long inicio = System.nanoTime();
           generaArreglo.quickSort(copiaVector, 0, copiaVector.length - 1);
            long fin = System.nanoTime();

            tiempoTotal = (fin - inicio);
        } catch (Exception e) {

        }
        botonConfirmar.setEnabled(true);
        opArreglo = 3;
    }//GEN-LAST:event_botonQuickActionPerformed

