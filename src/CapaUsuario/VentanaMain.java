    //Metodo que imprime copia ORDENAEDA del vector 
    private String imprimeOrdenado(double vect[]) {
        StringBuilder values = new StringBuilder();

        if (vect != null) {
            for (double value : vect) {
                values.append(value).append(" | ");
            }
        }
        return values.toString();
    }

