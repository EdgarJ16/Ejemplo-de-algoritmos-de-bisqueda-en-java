    private void botonBurbujaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBurbujaActionPerformed
        try {
            copiaVector = Arrays.copyOf(generaArreglo.getArreglo(), 1000);
            
            //UNA VEZ ALMACENADO SE PROCEDE A ORDENARLO
            long inicio = System.nanoTime();
            generaArreglo.bubbleSort(copiaVector);
            long fin = System.nanoTime();

            tiempoTotal = (fin - inicio);
        } catch (Exception e) {

        }
        botonConfirmar.setEnabled(true);

        opArreglo = 1;
    }//GEN-LAST:event_botonBurbujaActionPerformed

